export const penjumlahan = (bilanganSatu, bilanganDua) => {
    return bilanganSatu + bilanganDua;
};

export const pengurangan = (bilanganSatu, bilanganDua) => {
    return bilanganSatu - bilanganDua;
};

export const pembagian = (bilanganSatu, bilanganDua) => {
    return bilanganSatu / bilanganDua;
};

export const perkalian = (bilanganSatu, bilanganDua) => {
    return bilanganSatu * bilanganDua;
};

export const ratarata = (bilanganSatu, bilanganDua, bilanganTiga, bilanganEmpat, bilanganLima) => {
    return (bilanganSatu + bilanganDua + bilanganTiga + bilanganEmpat) / bilanganLima;
};

export const sisabagi = (bilanganSatu, bilanganDua) => {
    return bilanganSatu % bilanganDua;
};

export const luaspersegi = (sisiSatu, sisiDua) => {
    return sisiSatu * sisiDua;
};

export const luaslingkaran = (phi, jarijariSatu, jarijariDua) => {
    return phi * (jarijariSatu * jarijariDua);
};

export const luaskubus = (jumlahPersegi, rusukSatu, rusukDua) => {
    return jumlahPersegi * (rusukSatu * rusukDua);
};

export const volumekubus = (sisiSatu, sisiDua, sisiTiga) => {
    return sisiSatu * sisiDua * sisiTiga;
};